// Generated by running the test once with `LARQL_PRINT_GOLDEN=1`. To
// regenerate after an intentional walker change:
//   LARQL_PRINT_GOLDEN=1 cargo test -p larql-vindex --test \
//     test_walker_accuracy -- --nocapture
// then paste the printed hex strings below.

const GOLDEN_WEIGHT_WALKER_LAYER0: &str =
    "53bbb2110e2767a9683a872067ae4eb8253ee040db91cd4ba95d91e8540487dd";
const GOLDEN_ATTENTION_WALKER_LAYER0: &str =
    "c3f76f8f6dd590534eda2193786459f178c3af7a44ee8862ec11a69e09ca1905";
// Regenerated 2026-05-10: the canonicalisation now strips the `_header`
// record so the wall-clock `extraction_date` field doesn't make the
// golden drift every day. The hash covers only walker content rows.
const GOLDEN_VECTOR_EXTRACTOR_FFN_DOWN_LAYER0: &str =
    "8b5e221b150147ed40b0cfa67fdfc264e0628ab6cd6c59c2f9419e9350589b83";

fn check_or_print(label: &str, actual: &str, golden: &str) {
    if std::env::var("LARQL_PRINT_GOLDEN").is_ok() {
        eprintln!("{label} = {actual:?}");
        return;
    }
    assert_eq!(
        actual, golden,
        "{label}: walker output drifted — review the change and update the golden if intentional"
    );
}

#[test]
fn weight_walker_layer0_byte_identical() {
    let dir = fixture("ww");
    let walker = WeightWalker::load(dir.to_str().unwrap()).unwrap();
    let cfg = WalkConfig {
        top_k: 3,
        min_score: 0.0,
    };
    let mut g = Graph::new();
    let mut cb = SilentWalkCallbacks;
    walker.walk_layer(0, &cfg, &mut g, &mut cb).unwrap();

    let bytes = canonicalise_edges(&g, "layer", "feature");
    let hex = sha256_hex(&bytes);
    check_or_print("weight_walker_layer0", &hex, GOLDEN_WEIGHT_WALKER_LAYER0);

    cleanup(&dir);
}

