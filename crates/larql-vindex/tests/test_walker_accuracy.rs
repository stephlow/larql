//! Walker accuracy fixture — pins the deterministic output of every
//! walker to a golden SHA-256.
//!
//! Walker output is a pure function of `(model weights, config)`. The
//! mock model in `walker::test_fixture` is byte-deterministic (all
//! weights driven by `random_f32(n, seed)`), so re-running the same
//! walker with the same config must produce byte-identical edges /
//! vectors. This test asserts that contract: any change in the walker
//! arithmetic, ordering, or rounding will flip the hash and fail loudly.
//!
//! When intentionally changing walker output (e.g. fixing a bug,
//! tightening a threshold), update the golden constant — never silently
//! swallow a mismatch.
//!
//! Hashed payloads are stable canonical forms:
//!   weight walker → `subject\trelation\tobject\tlayer\tfeature\tconfidence:.6\n`
//!   attention walker → same shape, with `head` instead of `feature`
//!   vector extractor → the full NDJSON file, sorted lines, hashed bytes

use std::path::Path;

use sha2::{Digest, Sha256};

use larql_core::Graph;
use larql_vindex::walker::{
    attention_walker::AttentionWalker,
    test_fixture::create_mock_model,
    vector_extractor::{ExtractConfig, SilentExtractCallbacks, VectorExtractor},
    weight_walker::{SilentWalkCallbacks, WalkConfig, WeightWalker},
};

fn fixture(slug: &str) -> std::path::PathBuf {
    let dir = std::env::temp_dir().join(format!("larql_accuracy_{slug}"));
    let _ = std::fs::remove_dir_all(&dir);
    create_mock_model(&dir);
    dir
}

fn cleanup(dir: &Path) {
    let _ = std::fs::remove_dir_all(dir);
}

fn sha256_hex(bytes: &[u8]) -> String {
    let mut h = Sha256::new();
    h.update(bytes);
    let out = h.finalize();
    out.iter().map(|b| format!("{b:02x}")).collect()
}

/// Canonical edge form: tab-separated fields, sorted, one edge per line.
fn canonicalise_edges(graph: &Graph, layer_field: &str, feature_field: &str) -> Vec<u8> {
    let mut lines: Vec<String> = graph
        .edges()
        .iter()
        .map(|e| {
            let m = e.metadata.as_ref().unwrap();
            let layer = m.get(layer_field).and_then(|v| v.as_u64()).unwrap_or(0);
            let feat = m.get(feature_field).and_then(|v| v.as_u64()).unwrap_or(0);
            format!(
                "{}\t{}\t{}\t{}\t{}\t{:.6}",
                e.subject, e.relation, e.object, layer, feat, e.confidence
            )
        })
        .collect();
    lines.sort();
    let mut out = lines.join("\n").into_bytes();
    out.push(b'\n');
    out
}

